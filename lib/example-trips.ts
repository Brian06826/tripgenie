function mapsUrl(name: string, city: string) {
  return `https://www.google.com/maps/search/${encodeURIComponent(`${name} ${city}`)}`
}
function reviewsUrl(name: string, city: string) {
  return `https://www.google.com/search?q=${encodeURIComponent(`${name} ${city} reviews`)}`
}
function yelpUrl(name: string, city: string) {
  return `https://www.yelp.com/search?find_desc=${encodeURIComponent(name)}&find_loc=${encodeURIComponent(city)}`
}

